<p>
	La nuit du droit aura lieux ce 4 octobre 2023 à l'occasion de sa 5eme édition. Lors de cette
	édition vous pourrez vivre une immersion au cœur de 205 institutions et plus de 160 villes en
	France Métropolitaine et Outre mer.
</p>
<p>
	La nuit du droit est, depuis 2018, <a href="/evenement-festif-familial"
		>l'évenement festif et familial</a
	> annuel que les professionnels du droit donnent aux citoyens au soir du 4 octobre, jour anniversaire
	de notre Constitution. Cette année, la Constitution a 65 ans et devient la Constitution avec la durée
	la plus longue de notre histoire depuis la Première République du 21 septembre 1792.
</p>
<Image src="/images/fete-1.png" alt="Image fête 1" />
<p>
	Après la période difficile du Covid-19, le rendez-vous du 4 octobre 2022 a été marqué par
	l'enthousiasme, puisque 165 Nuits du Droit se sont déroulées dans 114 villes à l'initiative des
	deux assemblées parlementaires, des juridictions judiciaires et administratives, des facultés de
	droit, des barreaux, des chambres des notaires, des fondations, de la police, de la gendarmerie et
	de bien d'autres encore.
</p>
<p>
	Les sujets abordés en 2023 reflèteront les préoccupations de la société : environnement,
	cyber-harcèlement, conduites addictives, droits des séniors, violences familiales, etc. Certaines
	Nuits du droit transforment nos concitoyens en enquêteurs, juges d'instruction, jurés d'Assises ;
	une immersion toujours appréciée.
</p>
<Image src="/images/fete-2.png" alt="Image fête 2" />
<p>
	Au programme, des procès fictifs, historiques, des concours d'éloquence, des escape games, des
	débats, des pièces de théâtre, des projections de films, des débats et des conférences sur des
	sujets variés tel que : environnement, cyberharcèlement, conduite addictive etc... Cette Nuit sera
	consacrée à la sensibilisation et à l'information des métiers du droit pour nos concitoyens. Car
	oui, avant tout la nuit du droit est un <a href="/evenement-festif-familial"
		>événement festif et familial</a
	> qui a pour but de faire découvrir le droit à tous sous un ton léger et festif. Cela aidera le plus
	grand nombre à être au courant de l'évolution au seins de notre pays, et pourquoi pas donner envie
	à certain(e) d'en faire leur vocation.
</p>
<p>
	Le site de La Nuit du Droit est désormais en place. Trouvez la nuit du droit la plus proche de
	chez vous et venez découvrir cet événement à nos cotés.
</p>

