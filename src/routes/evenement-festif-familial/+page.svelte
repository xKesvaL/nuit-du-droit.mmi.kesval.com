<p>
	L'édition 2023 de la Nuit du Droit, un événement alliant <a href="/fete-educative"
		>festivités et éducation</a
	>, est une célébration joyeuse de la Constitution et des fondements du droit en France. Depuis sa
	création à Paris en 2017, et son extension à l'échelle nationale en 2018, cette nuit magique est
	devenue un rendez-vous annuel incontournable. L'année 2023 marque le 65e anniversaire de la
	Constitution, établissant ainsi un record de longévité depuis 1792.
</p>
<Image src="/images/event-1.png" alt="Image evenement 1" />

<p>
	Cette soirée, célébrée chaque 4 octobre, a la capacité d'unir citoyens curieux et experts en
	droit. En 2022, elle a illuminé 114 villes grâce à la collaboration de divers acteurs du monde
	juridique, et l'édition 2023 s'annonce encore plus grandiose, avec plus de 205 institutions et
	plus de 160 villes participantes. Les thèmes abordés en 2023 reflètent les préoccupations de la
	société, notamment l'environnement, le cyber-harcèlement, les droits des aînés et la lutte contre
	les violences familiales. La Nuit du Droit offre une expérience immersive où les citoyens
	endossent le rôle d'enquêteurs, de juges d'instruction ou de jurés d'Assises, offrant ainsi une
	opportunité unique de plonger dans l'univers du droit.
</p>

<p>
	Initiée par Laurent Fabius, la Nuit du Droit va au-delà de sa simple nature juridique. Elle
	sensibilise le public aux principes et aux métiers du droit, soulignant ainsi son rôle crucial
	dans notre quotidien. Cette célébration culturelle efface les frontières entre les citoyens et les
	professionnels du droit, favorisant ainsi une compréhension commune du domaine juridique. Mais ne
	vous y méprenez pas, il ne s'agit pas d'un vaste cours de droit, mais bel et bien d'une fête. Le
	principal but de cette Nuit du droit et de faire découvrir le droit à tous sous un <a
		href="/fete-educative"
	>
		ton léger, festif et familial.</a
	>
</p>
<Image src="/images/event-2.png" alt="Image evenement 2" />

<p>
	L'édition 2023 sera marquée par la présence du Président de la République pour célébrer les 65 ans
	de la Constitution de la Ve République. C'est un moment mémorable pour rappeler l'importance du
	droit dans nos vies. La Nuit du Droit est une expérience inoubliable qui allie festivités et
	éducation, célébrant ainsi la Constitution et les principes du droit français.
</p>

